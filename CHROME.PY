

import turtle as kreggscode
from time import sleep
kreggscode.getscreen()
kreggscode.bgcolor('black')
kreggscode = kreggscode.Turtle()
kreggscode.getscreen().colormode(255)
r = 120
kreggscode.setheading(-150)
kreggscode.up()
kreggscode.color(223, 35, 35)  
kreggscode.begin_fill()
kreggscode.fd(r)
kreggscode.down()
kreggscode.right(90)
kreggscode.circle(-r, 120)
kreggscode.fd(r * 3 ** .5)
kreggscode.left(120)
kreggscode.circle(2 * r, 120)
kreggscode.left(60)
kreggscode.fd(r * 3 ** .5)
kreggscode.end_fill()
kreggscode.left(180)
kreggscode.color(75, 183, 75) 
kreggscode.begin_fill()
kreggscode.fd(r * 3 ** .5)
kreggscode.left(120)
kreggscode.circle(2 * r, 120)
kreggscode.left(60)
kreggscode.fd(r * 3 ** .5)
kreggscode.left(180)
kreggscode.circle(-r, 120)
kreggscode.end_fill()
kreggscode.left(180)
kreggscode.circle(r, 120)
kreggscode.color(252, 216, 9) 
kreggscode.begin_fill()
kreggscode.circle(r, 120)
kreggscode.right(180)
kreggscode.fd(r * 3 ** .5)
kreggscode.right(60)
kreggscode.circle(-2 * r, 120)
kreggscode.right(120)
kreggscode.fd(r * 3 ** .5)
kreggscode.end_fill()
kreggscode.up()
kreggscode.left(98)
kreggscode.fd(r / 20)
kreggscode.setheading(60)
kreggscode.color(86, 146, 195) 
kreggscode.down()
kreggscode.begin_fill()
kreggscode.circle(kreggscode.distance(0, 0))
kreggscode.end_fill()
kreggscode.ht()
kreggscode.penup()
kreggscode.goto(-110, -300)
kreggscode.color('white')
kreggscode.pendown()
kreggscode.write("KREGGSCODE",
     font=("Arial", 20, "bold"))
kreggscode.getscreen().exitonclick()
